package com.example.foodtruck.Models;

public class Customer {

    private long m_Id;
    private String m_FirstName;
    private String m_LastName;
    private String m_Email;
    private String m_Password;
    private String m_PhoneNumber;
    private String m_StreetName;
    private String m_HouseNumber;
    private String m_ZipCode;
    private String m_City;
    private String m_State;

    //default constructor
    public Customer() {}

    public long getM_Id() {
        return m_Id;
    }

    public void setM_Id(long m_Id) {
        this.m_Id = m_Id;
    }

    public String getM_FirstName() {
        return m_FirstName;
    }

    public void setM_FirstName(String m_FirstName) {
        this.m_FirstName = m_FirstName;
    }

    public String getM_LastName() {
        return m_LastName;
    }

    public void setM_LastName(String m_LastName) {
        this.m_LastName = m_LastName;
    }

    public String getM_Email() {
        return m_Email;
    }

    public void setM_Email(String m_Email) {
        this.m_Email = m_Email;
    }

    public String getM_Password() {
        return m_Password;
    }

    public void setM_Password(String m_Password) {
        this.m_Password = m_Password;
    }

    public String getM_PhoneNumber() {
        return m_PhoneNumber;
    }

    public void setM_PhoneNumber(String m_PhoneNumber) {
        this.m_PhoneNumber = m_PhoneNumber;
    }

    public String getM_StreetName() {
        return m_StreetName;
    }

    public void setM_StreetName(String m_StreetName) {
        this.m_StreetName = m_StreetName;
    }

    public String getM_HouseNumber() {
        return m_HouseNumber;
    }

    public void setM_HouseNumber(String m_HouseNumber) {
        this.m_HouseNumber = m_HouseNumber;
    }

    public String getM_ZipCode() {
        return m_ZipCode;
    }

    public void setM_ZipCode(String m_ZipCode) {
        this.m_ZipCode = m_ZipCode;
    }

    public String getM_City() {
        return m_City;
    }

    public void setM_City(String m_City) {
        this.m_City = m_City;
    }

    public String getM_State() {
        return m_State;
    }

    public void setM_State(String m_State) {
        this.m_State = m_State;
    }


}
